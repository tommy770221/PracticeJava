public class ConceptOfOOP {

    /*
    * 結構化程式設計
    * 程式 = 算法 + 數據結構
    * 算法第一,數據結構第二
     * 面向對象OOP
     * 程式=對象+對象 +....
     * 數據第一 算法第二
    *  讓每一個對象負責執行某一組相關任務
    *  OOP特性
    *  萬物皆對象
    *  程式是一組對象彼此之間在發消息
    *  每個對象都有自己的內存佔用,可以組裝更大對象
    *  每個對象都有類型
    *  特定類型的所有對象可以接收相同消息
    *
    * */

    /*
       類是創健對象的模板和藍圖
       類是一組類似對象的共同抽象定義
     */

    /*
    * 對象是類的實例話結果
    * 對象是實實在在的存在,
    * 代表現實世界的某一事物
    *
    * */
     /*
       *對象的兩個關鍵特性
       * 行為: 對象能幹什麼
       * 狀態: 對象的屬性,行為的結果
       *
      */

     /* 區別
     *  類是靜態定義
     *  對象是動態實例
     *  程式代碼操作的是對象而非類
     *  建模得到的是類而非對象
     *
     *  連繫
     *  類是對象的定義
     *  對象的產生離不開類這個模板
     *  類存在的目的是實例化得到對象
     *
      */

     /*
     * 被回收的對象是:
     * 不再被任何對象引用的對象
     * 引用變量自動放棄
     * 人為將引用變量設為nul
     *
     * */

    /**
        *   static 變量不屬於任何 object
        */

    /**
     *  private 父類別方法 不可以被override
     *
     * */
}
